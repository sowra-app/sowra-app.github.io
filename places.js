let GEO = {}, VILL = {}, customPlaces = [];
function rebuildGeo(){
  GEO = JSON.parse(JSON.stringify(BASE_GEO));
  VILL = {};
  for(const c of customPlaces){
    if(!GEO[c.region]) GEO[c.region] = [];
    if(c.kind === 'city'){
      if(!GEO[c.region].includes(c.name)) GEO[c.region].push(c.name);
    }else{
      (VILL[c.region] = VILL[c.region] || []).push(c.name);
    }
  }
}
